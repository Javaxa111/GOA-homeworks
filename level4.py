# 6) დამატებით მოიძიეთ ინფორმაცია for loop შესახებ და ახსენით თუ რამდენი არგუმენტის გადაცემა შეგვიძლია, რას აკეთებს თითოეული მათგანი
#for loop გამოიყენება ობიექტებზე გასავლელად ან კონკრეტული რაოდენობის გამეორებისთვის.
# range() ფუნქციას შეუძლია მიიღოს 1, 2 ან 3 არგუმენტი
# range(stop) → 0-დან stop-მდე (არა ჩათვლით)
for i in range(5):
    print(i)

# range(start, stop) → იწყება start-დან და მთავრდება stop-მდე (არა ჩათვლით)
for i in range(2, 6):
    print(i)

# range(start, stop, step) → step განსაზღვრავს ნაბიჯს
for i in range(0, 10, 2):
    print(i)

# 7) დამატებით მოიძიეთ ინფორმაცია while loop - ის შესახებ და ახსენით თუ რა განხვავებაა for loop - სა და while loop - ს შორის    
# while loop აგრძელებს მუშაობას მანამ, სანამ პირობა True-ა.
# განსხვავება:
# - for loop გამოიყენება განსაზღვრული რაოდენობის გამეორებისთვის
# - while loop გამოიყენება მანამ, სანამ პირობა არ გახდება False

count = 0
while count < 5:
    print("მიმდინარე მნიშვნელობა:", count)
    count += 1

# 8) მომხმარებელს შემოატანინეთ რაიმე რიცხვი და გაიგეთ გადმოცემული რიცხვის ფაქტორიალი, გადახედეთ ინფორმაციას რიცხვის ფაქტორიალის შესახებ
number = int(input("შეიყვანე რიცხვი ფაქტორიალისთვის: "))
factorial = 1

for i in range(1, number + 1):
    factorial *= i

print("რიცხვის ფაქტორიალია:", factorial)

